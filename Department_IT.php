                        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ในปี พ.ศ.2541 มหาวิทยาลัยได้จัดตั้งศูนย์สำหรับบริการอินเทอร์เน็ต โดยผู้ริเริ่มในการ ก่อตั้งคือ อาจารย์ฉลองชัย แก้วภูผา ร่วมกับภาควิชาคอมพิวเตอร์ สังกัดคณะวิทยาศาสตร์และเทคโนโลยี โดยตั้งชื่อว่า ศูนย์คอมพิวเตอร์และอินเทอร์เน็ต มีที่ตั้งครั้งแรกอยู่ที่อาคาร 4 ห้อง 433 และได้ย้ายที่ทำการไปที่อาคารศูนย์วิทยาศาสตร์ ห้อง 9103 และ 9104 ศูนย์คอมพิวเตอร์และอินเทอร์เน็ตตั้งขึ้นโดยมีวัตถุประสงค์เพื่อให้การฝึกอบรมบุคลากร ควบคุมดูแล และให้บริการทางด้านคอมพิวเตอร์ ทั้งฮาร์ดแวร์ ซอฟต์แวร์ และเครือข่ายให้แก่หน่วยงานทั้งภายในและภายนอกมหาวิทยาลัย</p>    
                        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ในปีการศึกษา 2543 ได้ย้ายที่ทำการศูนย์คอมพิวเตอร์และอินเทอร์เน็ตไปอยู่ที่อาคาร 4 ห้อง 433 และมีห้องปฏิบัติการคอมพิวเตอร์สำหรับบริการนักศึกษา 2 ห้อง คือ ห้อง 431 (อาคาร 4) และห้อง 9103 (อาคารวิทย์)</p>
                        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ในปี 2544 กระทรวงศึกษาธิการและสำนักงานสภาสถาบันราชภัฏสมัยนั้นได้มีนโยบายให้แต่งตั้งผู้บริหารระดับสูงหรือ CIO รับผิดชอบทางด้านเทคโนโลยีสารสนเทศ (Information Technology – IT) มหาวิทยาลัยจึงได้มอบให้ศูนย์ฯรับผิดชอบในการให้บริการด้านการฝึกอบรมด้าน IT ให้แก่บุคลากรและบุคคลภายนอก บริการและดูแลระบบ IT ของหน่วยงานต่าง ๆ ทั้งมหาวิทยาลัย พร้อมทั้งดูแลแผนแม่บทด้าน IT ของมหาวิทยาลัย และมหาวิทยาลัยฯได้แต่งตั้ง คณะทำงานด้านเทคโนโลยีสารสนเทศของมหาวิทยาลัย ชุดแรกขึ้นเมื่อวันที่ 8 กรกฎาคม 2544</p>
                        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ในวันที่ 14 มิถุนายน 2547 มีการประกาศใช้พระราชบัญญัติมหาวิทยาลัยราชภัฏ ทำให้สถาบันราชภัฏรำไพพรรณีเปลี่ยนสถานะเป็นมหาวิทยาลัยราชภัฏรำไพพรรณี มีการแบ่งส่วนโครงสร้างการบริหารงานใหม่ โดยศูนย์คอมพิวเตอร์และอินเตอร์เน็ต เปลี่ยนชื่อเป็น ศูนย์เทคโนโลยีสารสนเทศ (Information Technology Center) และอยู่ภายใต้การกำกับดูแลของสำนักวิทยบริการและเทคโนโลยีสารสนเทศ จนถึงปัจจุบัน</p>
                        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ในปี 2549 มหาวิทยาลัยฯได้รับงบประมาณแผ่นดินสำหรับสร้างอาคารศูนย์ภาษาและคอมพิวเตอร์ จำนวน 60 ล้านบาท ซึ่งขณะนี้ดำเนินการก่อสร้าง เสร็จเรียบร้อยแล้ว</p>
                        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ในปีเดียวกัน มหาวิทยาลัยได้แต่งตั้งคณะกรรมการบริหารศูนย์เทคโนโลยีสารสนเทศ และคณะอนุกรรมการจัดทำแผนแม่บทเทคโนโลยีสารสนเทศ ซึ่งมีผู้บริหารเทคโนโลยีสารสนเทศระดับสูงเป็นประธานกรรมการ (2 กุมภาพันธ์ 2549) และได้จัดทำแผนแม่บทเทคโนโลยี สารสนเทศและการสื่อสารของมหาวิทยาลัยราชภัฏรำไพพรรณี พ.ศ.2550 – 2554 และนำมาใช้เป็นแนวทางในการพัฒนาด้านเทคโนโลยีสารสนเทศและการสื่อสารจนถึงปัจจุบัน</p>
                        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ศูนย์เทคโนโลยีสารสนเทศ ได้ย้ายที่ทำการจากอาคาร 4 ไปยังอาคารเฉลิมพระเกียรติฉลองสิริราชสมบัติครบ 60 ปี เมื่อวันที่ 8 ธันวาคม 2551 จนถึงปัจจุบัน โดยมีสำนักงานอยู่ที่ ชั้น 3 ห้อง 35301 มีห้องปฏิบัติการคอมพิวเตอร์ขนาด 72 เครื่อง สำหรับบริการนักศึกษาและบุคลากรทั่วไป จำนวน 1 ห้อง มีห้องปฏิบัติการคอมพิวเตอร์ ขนาด 72 เครื่อง สำหรับการฝึกอบรม จำนวน 2 ห้อง และมีห้องปฏิบัติการคอมพิวเตอร์ขนาด 32 เครื่อง สำหรับการฝึกอบรม จำนวน 1 ห้อง</p>
                        
