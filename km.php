                        <h5>การจัดการความรู้ (Knowledge Management : KM)</h5>
                    </div>
                </div>
                <div class="row">
                    <div class="col-sm-12">
                        <p>
                        การจัดการความรู้ คือ การรวบรวมองค์ความรู้ที่มีอยู่ในส่วนราชการซึ่งกระจัดกระจายอยู่ในตัวบุคคลหรือเอกสาร มาพัฒนาให้เป็นระบบ เพื่อให้ทุกคนในองค์กรสามารถเข้าถึงความรู้ และพัฒนาตนเองให้เป็นผู้รู้ รวมทั้งปฏิบัติงานได้อย่างมีประสิทธิภาพ อันจะส่งผลให้องค์กรมีความสามารถในเชิงแข่งขันสูงสุด โดยที่ความรู้มี 2 ประเภท คือ
                        </p>
                    </div>
                </div>
                <div class="row">
                    <div class="col-sm-6">
                        <p>
                        1. ความรู้ที่ฝังอยู่ในคน (Tacit Knowledge) เป็นความรู้ที่ได้จากประสบการณ์ พรสวรรค์หรือสัญชาติญาณของแต่ละบุคคลในการทำความเข้าใจในสิ่งต่าง ๆ เป็นความรู้ที่ไม่สามารถถ่ายทอดออกมาเป็นคำพูดหรือลายลักษณ์อักษรได้โดยง่าย เช่น ทักษะในการทำงาน งานฝีมือ หรือการคิดเชิงวิเคราะห์ บางครั้ง จึงเรียกว่าเป็นความรู้แบบนามธรรม
                        </p>
                    </div>
                    <div class="col-sm-6">
                        <p>
                        2. ความรู้ที่ชัดแจ้ง (Explicit Knowledge) เป็นความรู้ที่สามารถรวบรวม ถ่ายทอดได้ โดยผ่านวิธีต่าง ๆ เช่น การบันทึกเป็นลายลักษณ์อักษร ทฤษฎี คู่มือต่าง ๆ และบางครั้งเรียกว่าเป็นความรู้แบบรูปธรรม
                        </p>
                    </div>
                </div>


                <!--กระบวนการจัดการความรู้ (Knowledge Management)---------------->
                <div class="row">
                    <div class="col-sm-12">
                        <h5>กระบวนการจัดการความรู้ (Knowledge Management)</h5>
                    </div>
                </div>
                <div class="row">
                    <div class="col-sm-12">
                    <p>
                    กระบวนการจัดการความรู้ (Knowledge Management) เป็นกระบวนการที่จะช่วยให้เกิดพัฒนาการของความรู้ หรือการจัดการความรู้ที่จะเกิดขึ้นภายในองค์กร มีทั้งหมด 7 ขั้นตอน คือ
                    </p>
                    </div>
                </div>
                <div class="row">
                    <div class="col-sm-6">
                        <p>
                        1. การบ่งชี้ความรู้ เป็นการพิจารณาว่าองค์กรมีวิสัยทัศน์ พันธกิจ ยุทธศาสตร์ เป้าหมายคืออะไร และเพื่อให้บรรลุเป้าหมาย เราจำเป็นต้องใช้อะไร ขณะนี้เรามีความรู้อะไรบ้าง อยู่ในรูปแบบใด อยู่ที่ใคร                        </p>
                    </div>
                    <div class="col-sm-6">
                        <p>
                        2. การสร้างและแสวงหาความรู้ เช่นการสร้างความรู้ใหม่ แสวงหาความรู้จากภายนอก รักษาความรู้เก่า กำจัดความรู้ที่ใช้ไม่ได้แล้ว                        </p>
                    </div>
                </div>

                <div class="row">
                    <div class="col-sm-6">
                        <p>
                        3. การจัดความรู้ให้เป็นระบบ เป็นการวางโครงสร้างความรู้ เพื่อเตรียมพร้อมสำหรับการเก็บความรู้อย่างเป็นระบบในอนาคต                    </div>
                    <div class="col-sm-6">
                        <p>
                        4. การประมวลและกลั่นกรองความรู้ เช่น ปรับปรุงรูปแบบเอกสารให้เป็นมาตรฐาน ใช้ภาษาเดียวกัน ปรับปรุงเนื้อหาให้สมบูรณ์                    </div>
                </div>

                <div class="row">
                    <div class="col-sm-6">
                        <p>
                        5. การเข้าถึงความรู้ เป็นการทำให้ผู้ใช้ความรู้เข้าถึงความรู้ที่ต้องการได้ง่ายและสะดวก เช่น ระบบเทคโนโลยีสารสนเทศ (IT) Web board บอร์ดประชาสัมพันธ์ เป็นต้น                    </div>
                    <div class="col-sm-6">
                        <p>
                        6. การแบ่งปันแลกเปลี่ยนความรู้ ทำได้หลายวิธีการ โดยกรณีเป็น Explicit Knowledge อาจจัดทำเป็นเอกสาร ฐานความรู้ เทคโนโลยีสารสนเทศ หรือกรณีเป็น Tacit Knowledge จัดทำเป็นระบบ ทีมข้ามสายงาน กิจกรรมกลุ่มคุณภาพและนวัตกรรม ชุมชนแห่งการเรียนรู้ ระบบพี่เลี้ยง การสับเปลี่ยนงาน การยืมตัว เวทีแลกเปลี่ยนความรู้ เป็นต้น                    </div>
                </div>

                <div class="row">
                    <div class="col-sm-12">
                    <p>
                    7. การเรียนรู้ ควรทำให้การเรียนรู้เป็นส่วนหนึ่งของงาน เช่นเกิดระบบการเรียนรู้จากสร้างองค์ความรู้ การนำความรู้ในไปใช้ เกิดการเรียนรู้และประสบการณ์ใหม่ และหมุนเวียนต่อไปอย่างต่อเนื่อง 
                    </p>
