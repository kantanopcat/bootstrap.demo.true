                            3.4) ควบคุม ดูแล และให้บริการระบบเครือข่ายภายในมหาวิทยาลัย สำหรับบุคลากรและ นักศึกษา ให้มีความสะดวก รวดเร็ว และปลอดภัยในการใช้งาน <br>
