                            <b>1. บริหารจัดการสารสนเทศเพื่อการบริหาร (MIS) </b>
                            <ul>
                            <p>
                            1.1) ประสานงานกับผู้มีส่วนเกี่ยวข้องเพื่อให้การพัฒนา การบำรุงรักษา และใช้งานระบบ สารสนเทศเป็นไปด้วยความเรียบร้อย<br>
                            1.2) ส่งเสริมและสนับสนุนให้มีการนำระบบสารสนเทศไปใช้ในหน่วยงานต่าง ๆ<br>
                            1.3) ตรวจสอบข้อมูล สารสนเทศ และระบบสารสนเทศที่มีอยู่ในหน่วยงานภายในมหาวิทยาลัยให้ถูกต้องตรงตามความต้องการของระบบงาน<br>
                            1.4) ศึกษามาตรฐานข้อมูล และสารสนเทศเพื่อใช้ในการพัฒนาระบบสารสนเทศภายในมหาวิทยาลัยให้เป็นไปตามมาตรฐานสากล<br>
                            1.5) ศึกษาและพัฒนาขั้นตอนวิธีในการพัฒนาระบบงานเพื่อให้สามารถพัฒนาระบบงานได้อย่างมีประสิทธิภาพ<br>
                            1.6) ตรวจสอบและบำรุงรักษาเครื่องคอมพิวเตอร์แม่ข่ายสำหรับให้บริการ และข้อมูลบนระบบสารสนเทศของหน่วยงานต่างๆ ภายในมหาวิทยาลัยอยู่ในสถานะพร้อมใช้งาน<br>
                            1.7) การกำหนดสิทธิในการเข้าใช้งานข้อมูล สารสนเทศ และระบบสารสนเทศให้กับผู้บริหาร บุคลากร และนักศึกษาของมหาวิทยาลัย<br>
                            1.8) ตรวจสอบสิทธิผู้ที่มีสิทธิในการเข้าใช้งานระบบสารสนเทศ
                            1.9) พัฒนา ดูแลระบบการสำรองข้อมูลและระบบสารสนเทศเพื่อให้สามารถสำรองหรือกู้คืน ได้อย่างมีประสิทธิภาพ<br>
                            1.10) ให้คำปรึกษาในการใช้งานและแก้ไขปัญหาต่าง ๆ ที่เกิดขึ้นจากการใช้สารสนเทศหรือระบบสารสนเทศ
                            </p>
                            </ul>
                            <b>2. ดูแลพัฒนาเว็บไซต์และโปรแกรมประยุกต์ </b>
                            <ul>
                            <p>
                            2.1) ออกแบบ พัฒนา ปรับปรุงเว็บไซต์ตามความต้องการของหน่วยงานทั้งภายในและภายนอกมหาวิทยาลัย<br>
                            2.2) พัฒนาเว็บไซต์ให้สอดคล้องตามมาตรฐานสากลและที่ยอมรับ<br>
                            2.3) ประชาสัมพันธ์ข่าวสารผ่านทางเว็บไซต์มหาวิทยาลัย<br>
                            2.4) ออกแบบป้ายประชาสัมพันธ์ (Banner) ในการประชาสัมพันธ์ข้อมูลบนเว็บไซต์มหาวิทยาลัย<br>
                            2.5) พัฒนาระบบงานที่อำนวยสะดวกในการบริหารจัดการเว็บไซต์<br>
                            2.6) ศึกษามาตรฐานสากลในการพัฒนาเว็บไซต์<br>
                            2.7) ให้คำปรึกษา และแก้ไขปัญหาต่าง ๆ ในการพัฒนาเว็บไซต์<br>
                            2.8) พัฒนาโปรแกรมประยุกต์ตามยุทธศาสตร์มหาวิทยาลัย <br>
                            2.9) ดูแล ปรับปรุงและให้คำปรึกษาการใช้งานแก่ผู้ใช้งานโปรแกรมประยุกต์ที่พัฒนาขึ้น
                        </p>
                        </ul>
