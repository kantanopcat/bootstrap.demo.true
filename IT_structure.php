                            2.3) ควบคุม ดูแล และบำรุงรักษาศูนย์ข้อมูลด้านสารสนเทศของมหาวิทยาลัย <br>
                            2.4) ควบคุม ดูแล และป้องกันการบุกรุก/โจมตีระบบเครือข่ายและเครื่องแม่ข่ายคอมพิวเตอร์ของมหาวิทยาลัย <br>
